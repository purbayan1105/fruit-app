import mongoose from "mongoose";

export const ProductSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  imageUrl: {
    type: String,
    required: true,
  },
});

export const Products =
  mongoose.models.products || mongoose.model("products", ProductSchema);
